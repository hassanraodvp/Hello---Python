list1 = [1,2,3,4,5]
list2 = [6,7,8,9,10]
# Concatenation 
list3 = list1 + list2
print("The result of list3 is: ", list3)
# Repetition
list4 = list1 * 3
print("The result of list4 is: ",list4)
# Membership
list5 = 1 in list1
print("The result of list5 is: ",list5)
# Identity
list6 = list1 is list2
print("The result of list6 is: ",list6)
# Negation
list7 = not list1
print("The result of list7 is: ",list7)
# Length
list8 = len(list1)
print("The result of list8 is: ",list8)
